package br.com.jpbx.util;

import java.util.Date;

/**
 *
 * @author jefaokpta
 */
public class DacSinteticData {
    
    private Date day;
    private int total;
    private int answer;
    private int noAnswer;
    private int abandon;
    private int tme;
    private int tma;
    private int tmc;

    public Date getDay() {
        return day;
    }

    public void setDay(Date day) {
        this.day = day;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getAnswer() {
        return answer;
    }

    public void setAnswer(int answer) {
        this.answer = answer;
    }

    public int getNoAnswer() {
        return noAnswer;
    }

    public void setNoAnswer(int noAnswer) {
        this.noAnswer = noAnswer;
    }

    public int getAbandon() {
        return abandon;
    }

    public void setAbandon(int abandon) {
        this.abandon = abandon;
    }

    public int getTme() {
        return tme;
    }

    public void setTme(int tme) {
        this.tme = tme;
    }

    public int getTma() {
        return tma;
    }

    public void setTma(int tma) {
        this.tma = tma;
    }

    public int getTmc() {
        return tmc;
    }

    public void setTmc(int tmc) {
        this.tmc = tmc;
    }
    
    
}
